  CREATE TABLE "PRODUCTO" 
   (	"ID_PRODUCTO" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"NOMBRE" VARCHAR2(100) NOT NULL ENABLE, 
	"DESCRIPCION" VARCHAR2(255), 
	"EMPAQUE_TIPO" VARCHAR2(20), 
	"EMPAQUE_UNIDAD" VARCHAR2(20), 
	"EMPAQUE_CANTIDAD" NUMBER, 
	"PVS_USD" NUMBER, 
	"CBY" VARCHAR2(50), 
	"CDT" TIMESTAMP (6), 
	"MBY" VARCHAR2(50), 
	"MDT" TIMESTAMP (6), 
	"IMAGE_FILENAME" VARCHAR2(40), 
	"IMAGE_MIME_TYPE" VARCHAR2(40), 
	"IMAGE_BLOB" BLOB, 
	"IMAGE_LAST_UPDATE_DATE" DATE, 
	"ACTIVO" VARCHAR2(1) DEFAULT 'Y', 
	 PRIMARY KEY ("ID_PRODUCTO")
  USING INDEX  ENABLE, 
	 CONSTRAINT "PRODUCTO_UK01" UNIQUE ("NOMBRE")
  USING INDEX  ENABLE
   ) ;

  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_PRODUCTO_AUDIT" 
BEFORE INSERT OR UPDATE ON PRODUCTO 
FOR EACH ROW 
BEGIN 
    :NEW.MDT := SYSTIMESTAMP; 
    :NEW.MBY := NVL(v('APP_USER'), USER); 
    IF INSERTING THEN 
        :NEW.CDT := SYSTIMESTAMP; 
        :NEW.CBY := NVL(v('APP_USER'), USER); 
    END IF; 
END;
/
ALTER TRIGGER "TRG_PRODUCTO_AUDIT" ENABLE;