  CREATE TABLE "LOVS" 
   (	"ID_VALUE" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"LOV_NAME" VARCHAR2(30), 
	"LOV_VALUE" VARCHAR2(30), 
	"CBY" VARCHAR2(30), 
	"CDT" TIMESTAMP (6), 
	"MBY" VARCHAR2(30), 
	"MDT" TIMESTAMP (6), 
	 PRIMARY KEY ("ID_VALUE")
  USING INDEX  ENABLE, 
	 CONSTRAINT "LOVS_CON" UNIQUE ("LOV_NAME", "LOV_VALUE")
  USING INDEX  ENABLE
   ) ;

  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_LOVS" 
BEFORE INSERT OR UPDATE ON LOVS
FOR EACH ROW
BEGIN
    :NEW.MDT := SYSTIMESTAMP;
    :NEW.MBY := NVL(v('APP_USER'), USER);
    IF INSERTING THEN
        :NEW.CDT := SYSTIMESTAMP;
        :NEW.CBY := NVL(v('APP_USER'), USER);
    END IF;
END;
/
ALTER TRIGGER "TRG_LOVS" ENABLE;