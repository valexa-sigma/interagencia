  CREATE TABLE "USUARIO" 
   (	"ID_USUARIO" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"USERNAME" VARCHAR2(30), 
	"PASSWORD" VARCHAR2(30), 
	"EMAIL" VARCHAR2(100), 
	"NOMBRE" VARCHAR2(20), 
	"APELLIDO" VARCHAR2(20), 
	"CEDULA" VARCHAR2(20), 
	"TELEFONO1" VARCHAR2(20), 
	"TELEFONO2" VARCHAR2(20), 
	"DIRECCION" VARCHAR2(100), 
	"ACTIVO" VARCHAR2(1), 
	"CBY" VARCHAR2(30), 
	"CDT" TIMESTAMP (6), 
	"MBY" VARCHAR2(30), 
	"MDT" TIMESTAMP (6), 
	"ADMIN" VARCHAR2(1) DEFAULT 'N', 
	 PRIMARY KEY ("ID_USUARIO")
  USING INDEX  ENABLE, 
	 CONSTRAINT "USUARIO_UK01" UNIQUE ("USERNAME")
  USING INDEX  ENABLE, 
	 CONSTRAINT "USUARIO_UK02" UNIQUE ("EMAIL")
  USING INDEX  ENABLE, 
	 CONSTRAINT "USUARIO_UK03" UNIQUE ("CEDULA")
  USING INDEX  ENABLE
   ) ;

  CREATE OR REPLACE EDITIONABLE TRIGGER "TRG_USUARIO_AUDIT" 
BEFORE INSERT OR UPDATE ON USUARIO 
FOR EACH ROW 
BEGIN 
    :NEW.USERNAME := UPPER(TRIM(:NEW.USERNAME));
    :NEW.MDT := SYSTIMESTAMP; 
    :NEW.MBY := NVL(v('APP_USER'), USER); 
    IF INSERTING THEN 
        :NEW.CDT := SYSTIMESTAMP; 
        :NEW.CBY := NVL(v('APP_USER'), USER); 
    END IF; 
END;
/
ALTER TRIGGER "TRG_USUARIO_AUDIT" ENABLE;